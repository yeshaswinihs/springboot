package com.yesh.springboot.security.SpringBootSecurity.domain;

import java.io.Serializable;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.EnumType;
import javax.persistence.Enumerated;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.Table;

import com.yesh.springboot.security.SpringBootSecurity.utils.Role;

@Entity
@Table(name= "user")
public class User  implements Serializable{

	private static final long serialVersionUID = 1L;
	@Id
	@GeneratedValue(strategy = GenerationType.IDENTITY)
	@Column(name= "userid", nullable = false, updatable = false)
	Long userid;
	@Column(name = "username", nullable = false)
	String username;
	@Column(name = "email", nullable = false, unique = true)
	String email;
	@Column(name= "password", nullable = false)
	String password;
	@Column(name = "role", nullable = false)
	@Enumerated(EnumType.STRING)
	Role role;
	public Long getUserid() {
		return userid;
	}
	public void setUserid(Long userid) {
		this.userid = userid;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public Role getRole() {
		return role;
	}
	public void setRole(Role role) {
		this.role = role;
	}
	
	@Override
	 public String toString() {
	  return "User [userid=" + userid + ", username=" + username + ", email="
	    + email + ", password=" + password + ", role=" + role + "]";
	 }
	 @Override
	 public int hashCode() {
	  final int prime = 31;
	  int result = 1;
	  result = prime * result + ((email == null) ? 0 : email.hashCode());
	  result = prime * result
	    + ((password == null) ? 0 : password.hashCode());
	  result = prime * result + ((role == null) ? 0 : role.hashCode());
	  result = prime * result + ((userid == null) ? 0 : userid.hashCode());
	  result = prime * result
	    + ((username == null) ? 0 : username.hashCode());
	  return result;
	 }
	 @Override
	 public boolean equals(Object obj) {
	  if (this == obj)
	   return true;
	  if (obj == null)
	   return false;
	  if (getClass() != obj.getClass())
	   return false;
	  User other = (User) obj;
	  if (email == null) {
	   if (other.email != null)
	    return false;
	  } else if (!email.equals(other.email))
	   return false;
	  if (password == null) {
	   if (other.password != null)
	    return false;
	  } else if (!password.equals(other.password))
	   return false;
	  if (role != other.role)
	   return false;
	  if (userid == null) {
	   if (other.userid != null)
	    return false;
	  } else if (!userid.equals(other.userid))
	   return false;
	  if (username == null) {
	   if (other.username != null)
	    return false;
	  } else if (!username.equals(other.username))
	   return false;
	  return true;
	 }

	
	
}
